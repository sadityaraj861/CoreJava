package Basic;
import java.lang.*;

public class SizeOfDatatypes{

    public static void main(String[] args) {

        System.out.println("Integer min size : "+Integer.MIN_VALUE);
        System.out.println("Integer max size : "+Integer.MAX_VALUE);

        System.out.println("Float min size : "+Float.MIN_VALUE);
        System.out.println("float max size : "+Float.MAX_VALUE);

        System.out.println("short min size : "+Short.MIN_VALUE);
        System.out.println("short max size : "+Short.MAX_VALUE);

        System.out.println("long min size : "+Long.MIN_VALUE);
        System.out.println("long max size : "+Long.MAX_VALUE);

        System.out.println("char min size : "+Character.MIN_VALUE);
        System.out.println("char max size : "+Character.MAX_VALUE);
    }
}

